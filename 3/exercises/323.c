#include <stdio.h>

int main(void){
  int i;
  i = 10;
  printf("i = %d\n", i);
  printf("--i = %d\n", --i);
  printf("i = %d\n", i);
  printf("i-- = %d\n", i--);
  printf("i = %d\n", i);
  printf("\n");
  i=10;
  printf("i = %d\n", i);
  printf("++i = %d\n", ++i);
  printf("i = %d\n", i);
  printf("i++ = %d\n", i++);
  printf("i = %d\n", i);


  return 0;
}
